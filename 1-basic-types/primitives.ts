// Primitive types in TypeScript

// String
const name1: string = "Alice";

// Number (both integers and floats)
const age: number = 30;
const price: number = 19.99;

// Boolean
const isAdmin: boolean = true;

// Null and Undefined
const nothingHere: null = null;
const notAssignedYet: undefined = undefined;

// BigInt (requires ES2020 or later in tsconfig)
const bigNumber: bigint = 1234567890123456789012345678901234567890n;

// Symbol - unique and immutable identifier
const uniqueKey: symbol = Symbol('myKey');